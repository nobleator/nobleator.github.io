DROP TABLE IF EXISTS node;
CREATE TABLE IF NOT EXISTS node (
	id INTEGER,
	code TEXT,
	label TEXT,
	CONSTRAINT node_PK PRIMARY KEY (id)
);

DROP TABLE IF EXISTS edge;
CREATE TABLE IF NOT EXISTS edge (
	id INTEGER,
	source_node_id INTEGER,
	target_node_id INTEGER,
	label TEXT,
	CONSTRAINT edge_PK PRIMARY KEY (id),
	CONSTRAINT edge_source_FK FOREIGN KEY (source_node_id) REFERENCES node(id),
	CONSTRAINT edge_target_FK FOREIGN KEY (target_node_id) REFERENCES node(id)
);

DROP TABLE IF EXISTS node_event;
CREATE TABLE IF NOT EXISTS node_event (
	id INTEGER,
	node_id INTEGER,
	title TEXT,
	content TEXT,
	CONSTRAINT node_event_PK PRIMARY KEY (id),
	CONSTRAINT node_event_node_FK FOREIGN KEY (node_id) REFERENCES node(id)
);

